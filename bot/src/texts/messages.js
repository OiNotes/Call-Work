export const formatters = {
  shopList: (shops) => shops.map((shop) => {
    const seller = shop.seller_username ? `@${shop.seller_username}` : (shop.seller_first_name || '–ü—Ä–æ–¥–∞–≤–µ—Ü');
    const mark = shop.is_subscribed ? ' ‚Äî –≤ –ø–æ–¥–ø–∏—Å–∫–∞—Ö' : '';
    return `‚Ä¢ ${shop.name} ‚Ä¢ ${seller}${mark}`;
  }).join('\n'),
  orders: (orders) => orders.map((o) => {
    const price = o.total_price || o.totalPrice;
    const statusMap = {
      pending: '–æ–∂–∏–¥–∞–µ—Ç',
      processing: '–≤ –æ–±—Ä–∞–±–æ—Ç–∫–µ',
      completed: '–∑–∞–≤–µ—Ä—à—ë–Ω',
      cancelled: '–æ—Ç–º–µ–Ω—ë–Ω',
      shipped: '–æ—Ç–ø—Ä–∞–≤–ª–µ–Ω'
    };
    const status = statusMap[o.status] || o.status;
    const shopName = o.shop_name || '–ú–∞–≥–∞–∑–∏–Ω';
    return `‚Ä¢ ${status} ${shopName} ‚Äî $${Number(price || 0).toFixed(2)}`;
  }).join('\n'),
  subscriptions: (subscriptions) => subscriptions.map((sub) => {
    const name = sub.shop_name || sub.shopName || '–ú–∞–≥–∞–∑–∏–Ω';
    return `‚Ä¢ ${name}`;
  }).join('\n'),
  followsList: (follows) => follows.map((follow) => {
    const name = follow.source_shop_name || follow.sourceShopName || follow.name || '–ú–∞–≥–∞–∑–∏–Ω';
    const mode = follow.mode === 'resell' ? '–ø–µ—Ä–µ–ø—Ä–æ–¥–∞–∂–∞' : '–º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥';
    const markupValue = follow.markup_percentage ?? follow.markup ?? 0;
    const markup = Number.isFinite(Number(markupValue)) ? `${Number(markupValue).toFixed(0)}%` : `${markupValue}`;
    const markupText = follow.mode === 'resell' ? `, –Ω–∞—Ü–µ–Ω–∫–∞ ${markup}` : '';
    return `‚Ä¢ ${name} ‚Äî ${mode}${markupText}`;
  }).join('\n'),
  productsList: (products, shopName) => {
    if (!products.length) {
      return `–¢–æ–≤–∞—Ä—ã –º–∞–≥–∞–∑–∏–Ω–∞ ${shopName}. –ü–æ–∫–∞ —Ç–æ–≤–∞—Ä–æ–≤ –Ω–µ—Ç.`;
    }
    const lines = products.slice(0, 5).map((product) => `‚Ä¢ ${product.name} ‚Äî $${Number(product.price ?? 0).toFixed(2)}`);
    const extra = products.length > 5 ? `\n‚Ä¶ –µ—â—ë ${products.length - 5}` : '';
    return `–¢–æ–≤–∞—Ä—ã –º–∞–≥–∞–∑–∏–Ω–∞ ${shopName} (${products.length}).\n${lines.join('\n')}${extra}`;
  },
  salesList: (orders, shopName) => {
    if (!orders.length) {
      return `–ó–∞–∫–∞–∑—ã –º–∞–≥–∞–∑–∏–Ω–∞ ${shopName}. –ü–æ–∫–∞ –Ω–µ—Ç –ø—Ä–æ–¥–∞–∂.`;
    }
    const lines = orders.slice(0, 5).map((order) => {
      const buyer = order.buyer_username ? `@${order.buyer_username}` : (order.buyer_first_name || '–ü–æ–∫—É–ø–∞—Ç–µ–ª—å');
      const status = order.status || 'processing';
      const price = Number(order.total_price || order.totalPrice || 0).toFixed(2);
      return `‚Ä¢ ${buyer} ‚Äî ${status} ‚Äî $${price}`;
    });
    const extra = orders.length > 5 ? `\n‚Ä¶ –µ—â—ë ${orders.length - 5}` : '';
    return `–ó–∞–∫–∞–∑—ã –º–∞–≥–∞–∑–∏–Ω–∞ ${shopName} (${orders.length}).\n${lines.join('\n')}${extra}`;
  },
  productSection: (section, shopName, products) => {
    const isPreorder = section === 'preorder';
    const count = products.length;

    if (count === 0) {
      return isPreorder
        ? messages.buyer.preorderSectionEmpty(shopName)
        : messages.buyer.stockSectionEmpty(shopName);
    }

    const title = isPreorder
      ? messages.buyer.preorderSectionTitle(shopName, count)
      : messages.buyer.stockSectionTitle(shopName, count);

    const lines = products.slice(0, 5).map((product) => `‚Ä¢ ${product.name} ‚Äî $${Number(product.price ?? 0).toFixed(2)}`);
    const extra = count > 5 ? `\n‚Ä¶ –µ—â—ë ${count - 5}` : '';

    return `${title}\n${lines.join('\n')}${extra}`;
  },
  shopInfo: (shop, sections) => {
    const seller = shop.seller_username ? `@${shop.seller_username}` : (shop.seller_first_name || '–ü—Ä–æ–¥–∞–≤–µ—Ü');
    const stock = sections.stock || [];
    const preorder = sections.preorder || [];

    const stockLines = stock.slice(0, 3).map((p) => `‚Ä¢ ${p.name} ‚Äî $${Number(p.price || 0).toFixed(2)}`);
    const preorderLines = preorder.slice(0, 3).map((p) => `‚Ä¢ ${p.name} ‚Äî $${Number(p.price || 0).toFixed(2)}`);

    const extraStock = stock.length > 3 ? `\n‚Ä¶ –µ—â—ë ${stock.length - 3}` : '';
    const extraPreorder = preorder.length > 3 ? `\n‚Ä¶ –µ—â—ë ${preorder.length - 3}` : '';

    const stockSection = stock.length
      ? `${stockLines.join('\n')}${extraStock}`
      : '‚Ä¢ –ø–æ–∫–∞ –ø—É—Å—Ç–æ';
    const preorderSection = preorder.length
      ? `${preorderLines.join('\n')}${extraPreorder}`
      : '‚Ä¢ –æ–∂–∏–¥–∞–µ–º –ø–æ—Å—Ç–∞–≤–∫—É';

    return `${shop.name} ‚Ä¢ ${seller}\n\n–ù–∞–ª–∏—á–∏–µ ‚Äî ${stock.length || 0}\n${stockSection}\n\n–ü—Ä–µ–¥–∑–∞–∫–∞–∑ ‚Äî ${preorder.length || 0}\n${preorderSection}`;
  }
};
